package it.develhope.javadoc2;

/**
 * This class has the arithmetic operators
 * @author Lorenzo Balestrini
 */

public class ArithmeticOperators {

    /**
     * This method calls the sum between two numbers
     * @param num1 first number
     * @param num2 second number
     * @return first number + second number
     */
    public int sum(int num1, int num2){
        return num1 + num2;
    }

    /**
     * This method calls the subtraction between two numbers
     * @param num1 first number
     * @param num2 second number
     * @return first number - second number
     */
    public int sub(int num1, int num2){
        return num1 - num2;
    }

    /**
     * This method calls the multiplication between two numbers
     * @param num1 first number
     * @param num2 second number
     * @return first number * second number
     */
    public int mul(int num1, int num2){
        return num1 * num2;
    }

    /**
     * This method calls the division between two numbers
     * @param num1 first number
     * @param num2 second number
     * @return first number / second number
     */
    public int div(int num1, int num2){
        return num1 / num2;
    }
}
